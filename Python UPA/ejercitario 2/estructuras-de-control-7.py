# Escribir un programa que pida una nota de 1 a 5 y mostrarla de la forma:
# Excelente, Muy Bueno, Bueno, Aceptable, Insuficiente.
a = int(input("Ingrese la nota deseada"))
if a ==1:
    print("La nota es Excelente")
elif a==2:
    print("La nota es Muy Bueno")
elif a==3:
    print("La nota es Bueno")
elif a==4:
    print("La nota es Aceptable")
elif a==5:
    print("La nota es Insuficiente")