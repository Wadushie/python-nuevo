# Escribir una función que reciba dos fracciones e imprima la suma de las mismas.
# Las fracciones se deberán recibir como cadenas.

fraccion1 = int(input("Ingrese un numero"))
fraccion2 = int(input("Ingrese otro numero"))

def sumar_fracciones(fraccion1, fraccion2):
    suma = fraccion1+fraccion2
    return suma